// Crie um programa de contagem progressiva. 
// Peça um número e conte de 0 até esse número, usando um loop while no console do navegador.

let contagemRegressiva = 0
let inicio = prompt("Escolha um número para a contagem regressiva.");

while (contagemRegressiva <= inicio) {
    alert(contagemRegressiva);
    contagemRegressiva ++
}
